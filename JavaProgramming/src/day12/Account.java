package day12;

public class Account {
	
	private int accno;
	private String name;
	private double amunt;
	
		
	
	//  Create setter and getter form source
	
	public int getAccno() {
		return accno;
	}
	public void setAccno(int accno) {
		this.accno = accno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getAmunt() {
		return amunt;
	}
	public void setAmunt(double amunt) {
		this.amunt = amunt;
	}


}
